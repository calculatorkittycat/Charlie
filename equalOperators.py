# Assignment using =
a = 10
b = 20

# Print the values of a and b
print("Value of a:", a)  # Output: 10
print("Value of b:", b)  # Output: 20

# Comparison using ==
is_equal = (a == b)

# Print the result of the comparison
print("Is a equal to b?", is_equal)  # Output: False

# Change the value of b to be equal to a
b = 10

# Print the new value of b
print("New value of b:", b)  # Output: 10

# Compare again using ==
is_equal = (a == b)

# Print the result of the comparison
print("Is a equal to b now?", is_equal)  # Output: True
